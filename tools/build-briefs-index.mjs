#!/usr/bin/env node
/*
 * Builds briefs-index.json from briefs/_data.json — the curated "argument briefs"
 * layer (see briefs/README.md). A brief is our-own-words argument framing (core
 * move + strongest objection + honest concession) that the live /api/ask step can
 * retrieve as OPTIONAL background. Sibling of tools/build-sources-index.mjs.
 *
 * Run:   node tools/build-briefs-index.mjs
 * CI:    node tools/build-briefs-index.mjs --check   (fails if stale)
 *
 * SAFETY (mirrors /sources verified:true): a brief reaches the deployable module
 * lib/briefs-verified.js ONLY when its `reviewed` object stamps BOTH an argument
 * AND an orthodoxy date. An un-gated brief can never reach a live answer.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

// fileURLToPath (not .pathname) so this works on Windows too.
const ROOT = fileURLToPath(new URL('..', import.meta.url));
const DATA = ROOT + 'briefs/_data.json';

const REQUIRED = ['id', 'topic', 'tags', 'framing', 'from', 'reviewed'];

let arr;
try { arr = JSON.parse(readFileSync(DATA, 'utf8')); }
catch (e) { console.error(`✗ briefs/_data.json — invalid JSON: ${e.message}`); process.exit(1); }
if (!Array.isArray(arr)) { console.error('✗ briefs/_data.json must be an array'); process.exit(1); }

const problems = [];
const entries = [];
for (const e of arr) {
  for (const k of REQUIRED) if (!(k in e)) problems.push(`${e.id || '?'}: missing "${k}"`);
  if (e.tags && !Array.isArray(e.tags)) problems.push(`${e.id}: tags must be an array`);
  if (e.from && !Array.isArray(e.from)) problems.push(`${e.id}: from must be an array`);
  if (e.reviewed && typeof e.reviewed !== 'object') problems.push(`${e.id}: reviewed must be an object`);
  if (typeof e.framing === 'string' && e.framing.trim().length < 40) problems.push(`${e.id}: framing looks too short`);
  entries.push({
    id: e.id, topic: e.topic, tags: e.tags || [], framing: e.framing,
    from: e.from || [], reviewed: e.reviewed || { argument: null, orthodoxy: null, by: null },
  });
}

if (problems.length) {
  console.error('✗ briefs problems:\n  ' + problems.join('\n  '));
  process.exit(1);
}

const ids = entries.map((e) => e.id);
const dupes = ids.filter((id, i) => ids.indexOf(id) !== i);
if (dupes.length) { console.error('✗ Duplicate ids: ' + [...new Set(dupes)].join(', ')); process.exit(1); }

// Full index (all briefs, gated or not) for reference + the content agents.
const json = JSON.stringify(entries);
const OUT = 'briefs-index.json';

// A brief is GATED (may go live) only if BOTH review dates are present.
const isGated = (e) => e.reviewed && !!e.reviewed.argument && !!e.reviewed.orthodoxy;
const verified = entries
  .filter(isGated)
  .map(({ id, topic, tags, framing, from }) => ({ id, topic, tags, framing, from }));

// Verified-ONLY ESM module consumed by lib/retrieve-briefs.js → /api/ask. Emitting a
// plain JS module (not JSON) bundles reliably on Vercel, and — critically — an
// un-gated brief can NEVER reach a live answer.
const MOD_OUT = 'lib/briefs-verified.js';
const mod =
  '// AUTO-GENERATED by tools/build-briefs-index.mjs — do not edit by hand.\n' +
  '// Gated briefs only (reviewed.argument AND reviewed.orthodoxy set). Consumed by\n' +
  '// lib/retrieve-briefs.js for the live /api/ask retrieval step. Our-own-words\n' +
  '// argument framing, NOT quotable sources.\n' +
  'export const VERIFIED_BRIEFS = ' + JSON.stringify(verified) + ';\n';

if (process.argv.includes('--check')) {
  let curIdx = ''; try { curIdx = readFileSync(ROOT + OUT, 'utf8'); } catch {}
  if (curIdx.trim() !== json.trim()) {
    console.error(`✗ ${OUT} is stale — run: node tools/build-briefs-index.mjs`);
    process.exit(1);
  }
  let curMod = ''; try { curMod = readFileSync(ROOT + MOD_OUT, 'utf8'); } catch {}
  if (curMod.trim() !== mod.trim()) {
    console.error(`✗ ${MOD_OUT} is stale — run: node tools/build-briefs-index.mjs`);
    process.exit(1);
  }
  console.log(`✓ ${OUT} + ${MOD_OUT} up to date (${entries.length} briefs, ${verified.length} gated).`);
} else {
  writeFileSync(ROOT + OUT, json);
  writeFileSync(ROOT + MOD_OUT, mod);
  console.log(`Wrote ${OUT} (${entries.length} briefs) and ${MOD_OUT} (${verified.length} gated, ${entries.length - verified.length} pending).`);
}
