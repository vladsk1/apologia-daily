#!/usr/bin/env node
/*
 * list-clarifiers.mjs — scan the site for doctrinal clarifiers (the gold ＊ tooltips
 * rendered by /library/orthonote.js) and write a registry to docs/clarifiers.md.
 *
 * A single source of truth for "what delicate phrases have an on-page clarifier, and
 * exactly what each one says." Run after adding/changing clarifiers:
 *     node tools/list-clarifiers.mjs
 * Prints the total count; writes docs/clarifiers.md. Read-only except that one file.
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { globSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const files = globSync('{library/**/*.html,ev-s*.html,worldviews.html,answers/*.html}', { cwd: ROOT });

// one clarifier block: <span class="on"><span class="on-phrase">PHRASE</span>…<h4>HEADING</h4>
//   …on-yes…<span>IS</span>…on-no…<span>NOT</span>…<!--/onote--></span>
// tolerant of clarifiers with or without the <!--onote--> drift markers (essays added
// before the answer-page convention omit them; answer pages include them).
const RE = /<span class="on"><span class="on-phrase">([\s\S]*?)<\/span>(?:<!--onote-->)?<button class="on-mark"[\s\S]*?<span class="on-h">([\s\S]*?)<\/span>[\s\S]*?<b>Is saying<\/b><span>([\s\S]*?)<\/span><\/span><span class="on-row on-no"><b>Not saying<\/b><span>([\s\S]*?)<\/span><\/span><\/span>/g;

const strip = (s) => String(s).replace(/<[^>]*>/g, '').replace(/\s+/g, ' ').trim();

const rows = [];
for (const rel of files.sort()) {
  const html = readFileSync(join(ROOT, rel), 'utf8');
  let m;
  while ((m = RE.exec(html)) !== null) {
    rows.push({ file: rel, phrase: strip(m[1]), heading: strip(m[2]), is: strip(m[3]), not: strip(m[4]) });
  }
}

// build markdown
const byFile = {};
for (const r of rows) (byFile[r.file] ||= []).push(r);
let md = `# Doctrinal clarifiers registry\n\n`;
md += `_Auto-generated by \`tools/list-clarifiers.mjs\` — do not hand-edit. Each entry is a gold ＊ tooltip (rendered by \`library/orthonote.js\`) that fences a doctrinally delicate phrase with an "Is saying / Not saying" box. All clarifier text passes the \`apologia-orthodoxy\` + \`apologia-argument\` gates like any content._\n\n`;
md += `**Total clarifiers live: ${rows.length}** across ${Object.keys(byFile).length} page(s).\n\n`;
for (const file of Object.keys(byFile).sort()) {
  md += `## \`${file}\`\n\n`;
  for (const r of byFile[file]) {
    md += `- **Phrase:** "${r.phrase}" — _${r.heading}_\n`;
    md += `  - ✅ **Is saying:** ${r.is}\n`;
    md += `  - ⛔ **Not saying:** ${r.not}\n`;
  }
  md += `\n`;
}

const outDir = join(ROOT, 'docs');
if (!existsSync(outDir)) mkdirSync(outDir);
writeFileSync(join(outDir, 'clarifiers.md'), md);
console.log(`${rows.length} clarifier(s) across ${Object.keys(byFile).length} page(s) → docs/clarifiers.md`);
for (const file of Object.keys(byFile).sort()) console.log(`  ${byFile[file].length}  ${file}`);
